public class PTra02_01 {
	public static void main(String[] args) {

		// ★ 20を5で割った値を出力してください
		int a = 20; int b = 5;
		int c = a/b;
		
		System.out.println("20を5で割った値は"+c);

		// ★ 126に13を掛けた値を出力してください
		int d  = 126;  int e  = 13;
		int f  = d * e;

		System.out.println("126に13を掛けた値は"+f);
		
		// ★ 97を6で割ったときの余りを出力してください
		int g = 97;  int h = 6;
		int i = g % h;
		
		System.out.println("97を6で割ったときの余りは"+i);


	}
}
